import java.util.ArrayList;
import java.util.Scanner;

public class dinaminiaiSarasaiAntra {

	public static void main(String[] args) {
//		Programa priima tokias komandas:
//			[1] -ávesti þodá -leidþia ávesti þodá, kuris iðsaugomas á sàraðà       +
//			[2] -rasti þodá -randa þodá ir atspausdina kelintas jis yra sàraðe     +
//			[3] -trinti þodá -paklausia, koká þodá iðtrinti ir já iðtrina
//			[4] -iðvalyti sàraðà
//			[5] -baigti programà
		boolean runProgram = true;
		Scanner sc = new Scanner(System.in);
		ArrayList<String> stringListas = new ArrayList<String>();
		
		
		
		System.out.println(
				"Pasirinkite veiksmà: \n[1] -ávesti þodá\n[2] -rasti þodá\n[3] -trinti þodá\n[4] -iðvalyti sàraðà\n[5] -baigti programà");
		String menu = sc.next();
		while (runProgram) {
			if (menu.equals("1")) {
				System.out.println("Áveskite þodá sàraðà:");
				stringListas.add(sc.next());
				System.out.println(stringListas);
				System.out.println("Pasirinkite veiksmà: ");
				menu = sc.next();
			} else if (menu.equals("2")) {
				System.out.println("Áveskite ieðkoma þodá:");
				String ieskomasString = sc.next();
				for (String elementas : stringListas) {
					if (ieskomasString.equals(elementas)) {
						System.out.println("Jûsø ieðkomas þodis yra saraðo " + (stringListas.indexOf(elementas) + 1)
								+ "-oje vietoje");
						System.out.println("Pasirinkite veiksmà: ");
						menu = sc.next();
					}
				}
			} else if (menu.equals("3")) {
				System.out.println("Iveskite þodá kurá norite iðtrinti:");
				String trinamasString = sc.next();
				System.out.println("Ar tikrai norite iðtrinti ávesta þodá? [taip] arba [ne]");
				String trinamasZodis = sc.next();
				if (trinamasZodis.equals("taip")) {
					stringListas.remove(trinamasString);
					System.out.println("Jusu iðtrintas þodis: ");
					System.out.println(trinamasString);
					System.out.println("Þodþiø sàraðas: ");
					System.out.println(stringListas);
					
				}	else {
					System.out.println("Pasirinkite veiksmà: ");
					menu = sc.next();
				}
			} else if (menu.equals("4")) {
				System.out.println(stringListas);
				stringListas.clear();
				System.out.println("Pasirinkite veiksmà: ");
				menu = sc.next();
			} else if (menu.equals("5")) {
				runProgram = false;
			} else {
				System.out.println("error404!!! Neteisinga ávestis!");
				System.out.println("Pasirinkite veiksmà: ");
				menu = sc.next();
			}

		}
		
		sc.close();
	}
}